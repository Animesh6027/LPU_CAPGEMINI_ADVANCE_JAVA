class FloatingPointTypeVariable {
	public static void main(String[] args) {
		float floatTypeVariable;
		floatTypeVariable = 1.2f;
		System.out.println(floatTypeVariable);

		double doubleTypeVariable;
		double doubleTypeVariable1;
		doubleTypeVariable = 12.36;
		doubleTypeVariable1 = 12.365d;  // mentioning d or D is not compulsory...ur choice
		System.out.println(doubleTypeVariable);
		System.out.println(doubleTypeVariable1);
	}
}